package Data;





public class comments {
	private String gn;
	private String ucomment;
	public comments(String gn, String ucomment) {
		super();
		this.gn = gn;
		this.ucomment = ucomment;
	}

	
	public String getGn() {
		return gn;
	}
	public void setGn(String gn) {
		this.gn = gn;
	}
	public String getUcomment() {
		return ucomment;
	}
	public void setUcomment(String ucomment) {
		this.ucomment = ucomment;
	}
	


	}
